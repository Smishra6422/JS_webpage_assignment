let imageContainer = document.getElementsByClassName("image-container")[0];
let buttons = document.getElementById("buttons");
let serachBar = document.getElementById("exampleFormControlInput1");
let cards = document.getElementsByClassName("card");

function putImages(category) {
  for (let i = 0; i < category.length; i++) {
    let card = `<div class="card" >
          <img src=${category[i].imageUrl} class="card-img-top" alt="..." />
          <div class="card-body">
            <span class="card-text">
              ${category[i].name}
            </span>
            <span>$ ${category[i].price}</span>
          </div>
        </div>`;

    imageContainer.insertAdjacentHTML("beforeend", card);
  }
}

putImages(cardDetail.shirts);

buttons.addEventListener("click", (e) => {
  serachBar.value = "";
  let categoryName = e.target.dataset.categoryValue;
  if (categoryName) {
    imageContainer.innerHTML = "";
    putImages(cardDetail[categoryName]);
  }
});

serachBar.addEventListener("keyup", (e) => {
  // // Check it later either to keep in final one or not
  if (e.target.value.length === 1) {
    document.body.style.background = "#2C3335";
  } else if (e.target.value.length % 2 === 0) {
    document.body.style.backgroundImage =
      "linear-gradient(45deg,#7a1111,#4b134f)";
  } else if (e.target.value.length % 3 === 0) {
    document.body.style.backgroundImage =
      "linear-gradient(45deg,#4b134f,#7a1111)";
  } else {
    document.body.style.background = "#111";
  }

  const searchInputValue = e.target.value.toLowerCase();

  Array.from(cards).forEach((card) => {
    let cardName = card.children[1].firstElementChild.textContent;
    console.log(cards);
    if (cardName.toLowerCase().includes(searchInputValue)) {
      card.style.display = "block";
    } else {
      card.style.display = "none";
    }
  });
});
